// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x, y) {
  return (x <= y) ? x : y;

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  num = Math.abs(num);
  if(num === 0){
    return true;
  } else if(num === 1){
    return false;
  } else{
    return isEven(num - 2);
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  var count = 0;
  for(var i = 0; i < string.length; i++){
    if(string[i] === char){
      count++;
    }
  }
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let counter = 0;
  string.split("").forEach(letter =>{
    if(letter === 'B'){
      counter++;
    }
  })
  return counter;
}

